-- ---- 6a: Sync org_id from tenancy ----
-- Keeps charges.org_id, payments.org_id in sync with the parent
-- tenancy's org_id. Consistent pattern with previous migrations.

CREATE OR REPLACE FUNCTION sync_charge_org_id()
RETURNS TRIGGER AS $$
BEGIN
  SELECT org_id INTO NEW.org_id FROM tenancies WHERE id = NEW.tenancy_id;
  IF NEW.org_id IS NULL THEN
    RAISE EXCEPTION 'Tenancy % not found or has no org_id', NEW.tenancy_id;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_charges_set_org_id
  BEFORE INSERT OR UPDATE OF tenancy_id ON charges
  FOR EACH ROW EXECUTE FUNCTION sync_charge_org_id();

CREATE TRIGGER trg_rent_schedules_set_org_id
  BEFORE INSERT OR UPDATE OF tenancy_id ON rent_schedules
  FOR EACH ROW EXECUTE FUNCTION sync_charge_org_id();

CREATE OR REPLACE FUNCTION sync_payment_org_id()
RETURNS TRIGGER AS $$
BEGIN
  SELECT org_id INTO NEW.org_id FROM tenancies WHERE id = NEW.tenancy_id;
  IF NEW.org_id IS NULL THEN
    RAISE EXCEPTION 'Tenancy % not found or has no org_id', NEW.tenancy_id;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_payments_set_org_id
  BEFORE INSERT OR UPDATE OF tenancy_id ON payments
  FOR EACH ROW EXECUTE FUNCTION sync_payment_org_id();


-- ---- 6b: Update charges.paid_amount and status on allocation change ----
-- Called after any INSERT, UPDATE, or DELETE on payment_allocations.
-- Recomputes the total allocated amount and updates charges.status.

CREATE OR REPLACE FUNCTION refresh_charge_paid_amount()
RETURNS TRIGGER AS $$
DECLARE
  v_charge_id UUID;
  v_total     NUMERIC(10,2);
  v_amount    NUMERIC(10,2);
  v_due_date  DATE;
  v_new_status TEXT;
BEGIN
  -- Determine which charge to update
  IF TG_OP = 'DELETE' THEN
    v_charge_id := OLD.charge_id;
  ELSE
    v_charge_id := NEW.charge_id;
  END IF;

  -- Recompute total allocated
  SELECT COALESCE(SUM(amount_allocated), 0)
  INTO v_total
  FROM payment_allocations
  WHERE charge_id = v_charge_id;

  -- Get the charge amount and due_date
  SELECT amount, due_date INTO v_amount, v_due_date
  FROM charges
  WHERE id = v_charge_id;

  -- Determine new status
  IF v_total >= v_amount THEN
    v_new_status := 'paid';
  ELSIF v_total > 0 THEN
    v_new_status := 'partial';
  ELSIF v_due_date < CURRENT_DATE THEN
    v_new_status := 'overdue';
  ELSE
    v_new_status := 'pending';
  END IF;

  -- Update the charge
  UPDATE charges
  SET paid_amount = v_total,
      status      = v_new_status
  WHERE id = v_charge_id
    AND status NOT IN ('waived', 'cancelled');

  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_payment_allocations_refresh_charge
  AFTER INSERT OR UPDATE OR DELETE ON payment_allocations
  FOR EACH ROW EXECUTE FUNCTION refresh_charge_paid_amount();


