-- KEY DESIGN DECISIONS:
--   - charges.paid_amount is a stored, denormalised value updated by a trigger
--     on payment_allocations. This avoids an expensive SUM() join on every
--     rent ledger query and keeps the status field consistent.
--   - charges.status is also managed by trigger: pending → overdue when
--     due_date passes (via the update_charge_status trigger). In production
--     this would be called by a nightly cron job. For now, status is set
--     correctly on insert based on the due_date at time of creation.
--   - payments are immutable records (no updated_at). Corrections are made
--     by voiding and re-entering, not editing. This preserves the audit trail.
--   - payment_allocations link a single payment to one or more charges. A
--     payment can partially cover a charge (partial payment), which is
--     reflected in charges.status = 'partial'.
--   - org_id is denormalised onto all tables for efficient RLS, synced via
--     trigger from tenancies.org_id.
-- ================================================================


-- ================================================================
-- SECTION 1: RENT SCHEDULES
--
-- A rent schedule defines a recurring financial obligation on a tenancy.
-- Each active schedule generates one charge row per billing period.
-- Schedules can be deactivated (end_date set or is_active = false)
-- when rent is renegotiated or the tenancy ends.
--
-- In practice, schedules would be activated by an admin and charges
-- generated by a scheduled function (e.g. on the 1st of each month).
-- For the MVP, charges are created manually via the UI.
-- ================================================================

CREATE TABLE rent_schedules (
  id              UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  org_id          UUID          NOT NULL REFERENCES organisations(id) ON DELETE RESTRICT,
  tenancy_id      UUID          NOT NULL REFERENCES tenancies(id) ON DELETE CASCADE,

  charge_type     TEXT          NOT NULL DEFAULT 'rent'
                                CHECK (charge_type IN (
                                  'rent', 'service_charge', 'utilities', 'parking', 'other'
                                )),
  description     TEXT,
  amount          NUMERIC(10,2) NOT NULL CHECK (amount > 0),
  frequency       TEXT          NOT NULL DEFAULT 'monthly'
                                CHECK (frequency IN ('weekly', 'fortnightly', 'monthly', 'quarterly')),
  due_day         INT           NOT NULL DEFAULT 1
                                CHECK (due_day BETWEEN 1 AND 28),

  start_date      DATE          NOT NULL,
  end_date        DATE,

  is_active       BOOLEAN       NOT NULL DEFAULT true,
  created_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

CREATE TRIGGER trg_rent_schedules_updated_at
  BEFORE UPDATE ON rent_schedules
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();


-- ================================================================
-- SECTION 2: CHARGES
--
-- A charge is a single financial demand against a tenancy — one row
-- per expected payment. Charges are generated from a schedule
-- (schedule_id is set) or created ad-hoc (schedule_id = NULL).
--
-- CHARGE TYPES:
--   rent           — standard periodic rent payment
--   deposit        — tenancy deposit (one-off, on move-in)
--   late_fee       — late payment fee
--   service_charge — ongoing service charges (e.g. communal maintenance)
--   utilities      — utilities billed through the landlord
--   repair         — tenant-responsible repair costs recharged
--   other          — any other charge
--
-- STATUS LIFECYCLE:
--   pending → overdue  (when due_date passes with no payment)
--   pending → partial  (when some payment is allocated)
--   pending → paid     (when full amount is allocated)
--   overdue → partial  (late partial payment)
--   overdue → paid     (late full payment)
--   pending / overdue → waived   (charge written off)
--   pending / overdue → cancelled (charge removed before due)
--
-- paid_amount: stored denormalised value = SUM(payment_allocations.amount_allocated)
-- Updated by trigger on payment_allocations INSERT/UPDATE/DELETE.
-- ================================================================

CREATE TABLE charges (
  id              UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  org_id          UUID          NOT NULL REFERENCES organisations(id) ON DELETE RESTRICT,
  tenancy_id      UUID          NOT NULL REFERENCES tenancies(id) ON DELETE CASCADE,
  schedule_id     UUID          REFERENCES rent_schedules(id) ON DELETE SET NULL,

  charge_type     TEXT          NOT NULL DEFAULT 'rent'
                                CHECK (charge_type IN (
                                  'rent', 'deposit', 'late_fee', 'service_charge',
                                  'utilities', 'repair', 'other'
                                )),
  description     TEXT,
  due_date        DATE          NOT NULL,
  amount          NUMERIC(10,2) NOT NULL CHECK (amount > 0),

  paid_amount     NUMERIC(10,2) NOT NULL DEFAULT 0 CHECK (paid_amount >= 0),

  status          TEXT          NOT NULL DEFAULT 'pending'
                                CHECK (status IN (
                                  'pending', 'paid', 'partial', 'overdue', 'waived', 'cancelled'
                                )),

  notes           TEXT,
  created_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

CREATE TRIGGER trg_charges_updated_at
  BEFORE UPDATE ON charges
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();


-- ================================================================
-- SECTION 3: PAYMENTS
--
-- A payment records money received from a tenant. One payment can
-- cover one or more charges (via payment_allocations). Payments are
-- immutable — do not edit a payment record; instead void the
-- allocation and create a corrected payment.
--
-- Payments are NOT directly linked to a charge. The link is made
-- via payment_allocations. A payment with no allocations is
-- effectively "unallocated" and sits as a credit until allocated.
-- ================================================================

CREATE TABLE payments (
  id              UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  org_id          UUID          NOT NULL REFERENCES organisations(id) ON DELETE RESTRICT,
  tenancy_id      UUID          NOT NULL REFERENCES tenancies(id) ON DELETE CASCADE,

  amount          NUMERIC(10,2) NOT NULL CHECK (amount > 0),
  payment_date    DATE          NOT NULL,
  payment_method  TEXT          NOT NULL DEFAULT 'bank_transfer'
                                CHECK (payment_method IN (
                                  'bank_transfer', 'cash', 'cheque', 'card',
                                  'direct_debit', 'standing_order', 'other'
                                )),
  reference       TEXT,
  notes           TEXT,

  created_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW()
  -- Intentionally no updated_at — payments are immutable audit records
);


-- ================================================================
-- SECTION 4: PAYMENT ALLOCATIONS
--
-- Maps a payment to a specific charge. A payment can be split across
-- multiple charges (e.g. one bank transfer covers 2 months of rent).
-- A charge can receive allocations from multiple payments (partial
-- payments over time).
--
-- The UNIQUE constraint prevents double-allocating the same payment
-- to the same charge — instead update amount_allocated if needed.
-- ================================================================

CREATE TABLE payment_allocations (
  id               UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  payment_id       UUID          NOT NULL REFERENCES payments(id) ON DELETE CASCADE,
  charge_id        UUID          NOT NULL REFERENCES charges(id) ON DELETE RESTRICT,
  amount_allocated NUMERIC(10,2) NOT NULL CHECK (amount_allocated > 0),

  created_at       TIMESTAMPTZ   NOT NULL DEFAULT NOW(),

  UNIQUE (payment_id, charge_id)
);


-- ================================================================
-- SECTION 5: ARREARS LOG
--
-- Point-in-time snapshots of arrears per tenancy. Intended to be
-- populated by a nightly scheduled function. Enables trend charts
-- showing whether arrears are improving or worsening over time.
--
-- One row per tenancy per day. The UNIQUE constraint prevents
-- duplicate snapshots.
-- ================================================================

CREATE TABLE arrears_log (
  id              UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  org_id          UUID          NOT NULL REFERENCES organisations(id) ON DELETE CASCADE,
  tenancy_id      UUID          NOT NULL REFERENCES tenancies(id) ON DELETE CASCADE,
  snapshot_date   DATE          NOT NULL,
  arrears_amount  NUMERIC(10,2) NOT NULL DEFAULT 0,

  created_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW(),

  UNIQUE (tenancy_id, snapshot_date)
);


