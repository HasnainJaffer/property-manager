// Tenancies expiring within 60 days or currently periodic
export const mockExpiringTenancies = mockTenancies.filter(t => {
  if (!t.is_active) return false
  if (t.status === 'periodic') return true
  if (t.status === 'active' && t.end_date) {
    const daysLeft = Math.ceil(
      (new Date(t.end_date).getTime() - new Date('2026-05-15').getTime()) /
        (1000 * 60 * 60 * 24),
    )
    return daysLeft <= 60
  }
  return false
})
// = [ten-001 (46 days to Jun 30), ten-003 (periodic)]

